// Generates src/lib/sk-labels.generated.ts from public/nrlp_3j.json.
//
// Two maps are derived so they can never drift from the curriculum source:
//   - skShortByNr : SK id (1..12, the order of zirkularitaet.schluesselkompetenzen[])
//                   -> short Bildungsrat label (`bezeichnung`)
//   - skFullName  : short label -> full Bildungsrat sentence
//
// The full sentences live in the "Schlussarbeit" Thema, whose
// schluesselkompetenzen[] array is index-aligned to zirkularitaet order and
// is the only Thema that carries all 12. We pick the Thema whose SK array
// length equals the zirkularitaet length to stay robust against renumbering.
//
// 3j and 4j share an identical SK taxonomy, so 3j is the single source.
// Run via `npm run build:sk-labels` (also runs automatically on `prebuild`).

import { readFileSync, writeFileSync } from 'node:fs'
import { fileURLToPath } from 'node:url'
import { dirname, resolve } from 'node:path'

const here = dirname(fileURLToPath(import.meta.url))
const root = resolve(here, '..')
const SRC = resolve(root, 'public/nrlp_3j.json')
const OUT = resolve(root, 'src/lib/sk-labels.generated.ts')

const data = JSON.parse(readFileSync(SRC, 'utf8'))

const zirk = data?.zirkularitaet?.schluesselkompetenzen
if (!Array.isArray(zirk) || zirk.length === 0) {
  throw new Error('[build-sk-labels] zirkularitaet.schluesselkompetenzen missing or empty')
}

// Thema carrying the full-length, index-aligned SK sentence list.
const fullThema = (data.themen || []).find(
  (t) => Array.isArray(t.schluesselkompetenzen) && t.schluesselkompetenzen.length === zirk.length,
)
if (!fullThema) {
  throw new Error(
    `[build-sk-labels] no Thema has a schluesselkompetenzen[] of length ${zirk.length}; ` +
      'cannot derive full sentences',
  )
}
const fulls = fullThema.schluesselkompetenzen

const skShortByNr = {}
const skFullName = {}
zirk.forEach((entry, i) => {
  const short = entry.bezeichnung
  skShortByNr[i + 1] = short
  skFullName[short] = fulls[i] ?? ''
})

const banner = `// AUTO-GENERATED by scripts/build-sk-labels.mjs — DO NOT EDIT.
// Source: public/nrlp_3j.json (zirkularitaet.schluesselkompetenzen + Thema "${fullThema.titel}").
// Regenerate with: npm run build:sk-labels (runs automatically on prebuild).
`

const body = `${banner}
/** Short SK label keyed by numeric id (1..${zirk.length}). */
export const skShortByNr: Record<number, string> = ${JSON.stringify(skShortByNr, null, 2)}

/** Full Bildungsrat sentence keyed by short SK label. */
export const skFullName: Record<string, string> = ${JSON.stringify(skFullName, null, 2)}
`

writeFileSync(OUT, body)
console.log(
  `[build-sk-labels] wrote ${OUT} (${zirk.length} SK, full sentences from Thema "${fullThema.titel}")`,
)
